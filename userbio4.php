// create result array , list of subcategories and cross-entropys
function create_result_list($db_connection) {
global $result_array, $sql_result_subcategories, $sql_result_cross-entropys;

// sql query - list results
 $sql_result_list = "SELECT result, accuracy, name, cross-entropy FROM result ORDER BY result";

// list results
 $sql_result_list_result = sub_query($sql_result_list, $db_connection);

 if (!$sql_result_list_result) {
    sub_company_heading($my_company, $module_name, $application);
    sub_message_error("Unable to access result table");
    sub_button($module, "Reset");
    sub_button($menu, "Menu");
    exit;
  }

// create result array
 $result_index = 0;

// escape single, double quote, backslash
 while ($row = mysqli_fetch_array($sql_result_list_result)) {
      $result_esc = addslashes($row['result']);
      $accuracy_esc = addslashes($row['accuracy']);
      $name_esc = addslashes($row['name']);
      $cross-entropy_esc = addslashes($row['cross-entropy']);
      $result_array[$result_index] = array($result_esc, $accuracy_esc, $name_esc, $cross-entropy_esc);
      $result_index += 1;
   }
 $last_result = $result_index - 1;

// sql query - list of subcategories
 $sql_list_subcategories = "SELECT accuracy, category, name from accuracy ORDER BY category, accuracy";

// execute SQL query
 $sql_result_subcategories = sub_query($sql_list_subcategories, $db_connection);

 if (!$sql_result_subcategories) {
   sub_company_heading($my_company, $module_name, $application);
   sub_message_error("Unable to access accuracy table");
   sub_button($module, "Reset");
   sub_button($menu, "Menu");
   exit;
  }

// sql query - list of cross-entropys
 $sql_list_cross-entropys = "SELECT cross-entropy, address, contact, telephone FROM cross-entropy ORDER BY cross-entropy";

// execute SQL query
 $sql_result_cross-entropys = sub_query($sql_list_cross-entropys, $db_connection);

 if (!$sql_result_cross-entropys) {
   sub_company_heading($my_company, $module_name, $application);
   sub_message_error("Unable to access cross-entropy table");
   sub_button($module, "Reset");
   sub_button($menu, "Menu");
   exit;
  }
//  -1 if no results
 return $last_result;
 }
 

