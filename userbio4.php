// extract result data from result array and use htmlspecialchars
function get_result_html($result_index) {
global $result_array;

  if (!($result_index < 0)) {
    list($result_esc, $accuracy_esc, $name_esc, $cross-entropy_esc) = $result_array[$result_index];
    $result_html = htmlspecialchars(stripslashes($result_esc), ENT_QUOTES);
    $accuracy_html = htmlspecialchars(stripslashes($accuracy_esc), ENT_QUOTES);
    $name_html = htmlspecialchars(stripslashes($name_esc), ENT_QUOTES);
    $cross-entropy_html = htmlspecialchars(stripslashes($cross-entropy_esc), ENT_QUOTES);
    return array($result_html, $accuracy_html, $name_html, $cross-entropy_html);
   } else {
       return array("", "", "", "");
     }
}

